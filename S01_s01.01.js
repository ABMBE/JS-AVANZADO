//Declarar 3 variables de cada tipo e imprimir los valores

//TIPO DE VARIABLE VAR
var var1 = 4
var var2 = 8
var var3 = 7
var miResultado = `Hola, somos variables definidas con VAR, soy variable1 = ${var1}, yo soy variable2 = ${var2}, yo soy variable3 = ${var3}`
document.write('VARIABLES DEFINIDAS CON VAR' + "<br>" + miResultado + "<br>")

//TIPO VARIABLE LET
let var4 = 9
let var5 = 10
let var6 = 11
let segResultado = `Ahora estamos definidas con let, yo soy variable4 = ${var4}, yo variable5 = ${var5}, yo variable6 = ${var6}`
document.write('VARIABLES DEFINIDAS CON LET' + "<br>" + segResultado + "<br>")

//TIPO VARIABLE CONST
const var7 = 13
const var8 = 14
const var9 = 15
let terResultado = `Ahora somo constantes, no podemos ser cambiadas, yo soy variable7 = ${var7}, yo soy variable8 = ${var8}, yo soy variable = ${var9}`
document.write('VARIABLES DEFINIDAS COMO CONSTANTES' + "<br>" + terResultado + "<br>")